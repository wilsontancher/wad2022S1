var calculator = {
    add: function(a,b){
        return a+b;
    },
    subtract: function(a,b){
        return a-b;
    },
    multiply: function(a,b){
        return a*b;
    },
    divide: function(a,b){
        return a/b;
    }
}

console.log(calculator.add(1,2));
console.log(calculator.subtract(1,2));
console.log(calculator.multiply(1,2));
console.log(calculator.divide(1,2));
